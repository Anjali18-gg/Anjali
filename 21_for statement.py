#for statement
#iterating over a list

fruits = ["apple","banana","cherry"]
for fruit in fruits:
    print(fruit)

#iterating over a range of number
for number in range(5):
    print(number)

#iterating over a string
for char in "hello":
    print(char)

#iterating over a dictionary
student = {"name":"Anjali","age":20,"course":"BCA"}
for key, value in student.items():
 print(key, ":", value)

